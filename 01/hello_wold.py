# ========================================================

# -----------------------
# 1️⃣ Арифметические операторы
# -----------------------
# +    : сложение         -> 3 + 2 = 5
# -    : вычитание        -> 5 - 2 = 3
# *    : умножение        -> 3 * 4 = 12
# /    : деление          -> 8 / 2 = 4.0
# //   : целочисленное деление -> 7 // 3 = 2
# %    : остаток от деления    -> 7 % 3 = 1
# **   : возведение в степень  -> 2 ** 3 = 8

# -----------------------
# 2️⃣ Операторы сравнения
# -----------------------
# ==   : равно           -> 5 == 5 -> True
# !=   : не равно        -> 5 != 3 -> True
# <    : меньше          -> 3 < 5 -> True
# >    : больше          -> 5 > 3 -> True
# <=   : меньше или равно -> 3 <= 3 -> True
# >=   : больше или равно -> 5 >= 3 -> True

# -----------------------
# 3️⃣ Логические операторы
# -----------------------
# and  : логическое "и"   -> (5>3) and (2<4) -> True
# or   : логическое "или" -> (5>3) or (2>4) -> True
# not  : логическое "не"  -> not (5>3) -> False

# -----------------------
# 4️⃣ Операторы присваивания
# -----------------------
# =    : присвоение       -> x = 5  (x теперь 5)
# +=   : x += 3           -> x = x + 3
# -=   : x -= 2           -> x = x - 2
# *=   : x *= 4           -> x = x * 4
# /=   : x /= 2           -> x = x / 2
# //=  : x //= 3          -> x = x // 3
# %=   : x %= 3           -> x = x % 3
# **=  : x **= 2          -> x = x ** 2

# -----------------------
# 5️⃣ Скобки
# -----------------------
# () : круглые            -> определяют порядок вычислений и функции
# [] : квадратные         -> создание списков, доступ к элементам списка
# {} : фигурные           -> создание словарей (dict) или множеств (set)

# -----------------------
# 6️⃣ Другие полезные символы
# -----------------------
# ,  : запятая            -> разделяет значения в списках или функциях
# :  : двоеточие          -> используется в циклах, функциях, срезах
# .  : точка              -> доступ к методам и атрибутам объектов
# #  : комментарий        -> всё после # игнорируется Python
# \  : обратный слеш      -> продолжение строки или спец-символы
# " " / ' ' : кавычки     -> создание строк

# -----------------------
# 7️⃣ Примеры использования операторов
# -----------------------
# Арифметика
# print(3 + 2)   # 5
# print(7 % 3)   # 1
# print(2 ** 3)  # 8

# Сравнение
# print(5 == 5)  # True
# print(5 != 3)  # True
# print(3 < 5)   # True

# Логика
# print((5>3) and (2<4))  # True
# print((5>3) or (2>4))   # True
# print(not (5>3))        # False

# Присваивание
# x = 5
# x += 3   # x = 8
# x *= 2   # x = 16

# Списки и индексы
# lst = [1,2,3]
# print(lst[0])  # 1
# print(lst[1:3])  # [2,3]

