# Программа: количество дней в месяце

# Просим пользователя ввести номер месяца
monat = int(input("Gib die Monatszahl ein (1-12): "))  # input() спрашивает ввод с клавиатуры int() превращает текст в целое число

# Просим пользователя ввести год
jahr = int(input("Gib das Jahr ein: "))  # input() спрашивает год int() превращает текст в число

# Сначала проверим, високосный ли год
# Високосный год: делится на 400 или делится на 4, но не на 100
if (jahr % 400 == 0) or (jahr % 4 == 0 and jahr % 100 != 0):  # % — остаток от деления
    schaltjahr = True       # Если условие выполняется — год високосный
else:
    schaltjahr = False      # Если условие не выполняется — год не високосный

# Создадим список с именами месяцев для красивого вывода
monate = ["Januar", "Februar", "März", "April", "Mai", "Juni",
          "Juli", "August", "September", "Oktober", "November", "Dezember"]

# Проверим, что введённый месяц корректный
if monat < 1 or monat > 12:                   # Если месяц меньше 1 или больше 12
    print("Ungültiger Monat. Bitte gib eine Zahl zwischen 1 und 12 ein.")  # Сообщаем ошибку
else:
    # Теперь определяем количество дней
    if monat in [1, 3, 5, 7, 8, 10, 12]:      # Список месяцев с 31 днём
        tage = 31
    elif monat in [4, 6, 9, 11]:              # Список месяцев с 30 днём
        tage = 30
    else:                                     # Месяц февраль (2)
        if schaltjahr:                        # Проверяем, високосный ли год
            tage = 29
        else:
            tage = 28

    # Выводим результат
    print("Der", monate[monat-1], "im Jahr", jahr, "hat", tage, "Tage.")  # Monate-1, потому что список начинается с индекса 0
    


